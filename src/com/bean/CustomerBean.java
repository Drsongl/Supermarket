package com.bean;
public class CustomerBean {

    private int Cno ;
    private int Cgrade ;
	private String Cname ;
	private int Cgender ;
	
	public int getCno() {
		return Cno;
	}
	public void setCno(int cno) {
		Cno = cno;
	}
	public int getCgrade() {
		return Cgrade;
	}
	public void setCgrade(int cgrade) {
		Cgrade = cgrade;
	}
	public String getCname() {
		return Cname;
	}
	public void setCname(String cname) {
		Cname = cname;
	}	
	public int getCgender() {
		return Cgender;
	}
	public void setCgender(int cgender) {
		Cgender = cgender;
	}
}
